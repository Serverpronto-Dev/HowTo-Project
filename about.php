                </span>As chair of the Children’s Services Council (CSC), Children’s Strategic Plan Prosperity Committee, it gives me great honor to lead the production of the <strong>first edition of Self Sufficiency Tool Kit in Broward County</strong>. This tool kit is a collaboration of public sector agencies that are dedicated to ensuring that in difficult economic times, Broward County residents can sustain themselves.<br />
                  <br />
In 2011, the Prosperity Committee implemented a CSC lead Results Based Accountability (RBA) Model. It requires that we search for data to determine how to address community needs. Upon our research, we discovered that 88% to 96% of low-income renting households were spending 30% or more on housing costs countywide in 2009. We also learned that nearly half of all renting Broward households are low-income spending 30% or more on housing costs. These figures were alarming!</p>
                <p align="justify">We knew at that time that we wanted to impact these statistics, but were faced with the gloomy reality that the unemployment rate is high and the lack of affordable housing was not improving. We decided instead that it was more important to inform our community about accessing unknown resources and how to utilize their natural-born skills to address their financial needs. As a result, this tool kit was birthed.<br />
                  <br />
The toolkit is not a resource manual. It is a combination of a how-to-guide with hands-on tools and information about how to make improvements to your financial health. You will not find phone numbers to local agencies, as the committee has committed to using 211 as the sole vehicle to access agencies. You will find some websites that may offer greater information on specific topic areas.<br />
<br />
The tool kit is designed for you to use individually or with the assistance of a case manager or coach. It may serve as a unique opportunity for families to connect on important issues that they never discussed before.<br />
<br />
We are proud to offer this toolkit as a gift to the community and hope that it will have a substantial impact on the lives of Broward County residents.</p>
